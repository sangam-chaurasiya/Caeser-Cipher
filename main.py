alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r',
            's', 't', 'u', 'v', 'w', 'x', 'y', 'z', 'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r',
            's', 't', 'u', 'v', 'w', 'x', 'y', 'z']


def caesure(message, shift_amount, convert_format):
    new_message = ""
    if convert_format.lower() == 'decode':
        shift_amount *= -1
    for character in message:
        if character not in alphabet:
            new_message += character
        else:
            encrypted_character_index = alphabet.index(character) + shift_amount
            new_message += alphabet[encrypted_character_index]
    return new_message


